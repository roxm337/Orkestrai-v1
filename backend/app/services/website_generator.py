import json
import re
from pathlib import Path
from typing import Any

from app.core.config import Settings


class WebsiteGenerator:
    def __init__(self, settings: Settings):
        self.settings = settings

    async def generate_site(self, *, run_id: str, business: dict[str, Any], config: dict[str, Any]) -> dict[str, Any]:
        business_name = business.get("name", "Generated Business")
        analysis = business.get("analysis", {})
        site_slug = self._slugify(business_name)
        site_dir = self.settings.generated_sites_dir / run_id / site_slug
        site_dir.mkdir(parents=True, exist_ok=True)

        files = {
            site_dir / "index.html": self._index_html(business, analysis, config),
            site_dir / "style.css": self._globals_css(config),
            site_dir / "README.md": self._readme(business_name),
        }

        for path, content in files.items():
            path.parent.mkdir(parents=True, exist_ok=True)
            path.write_text(content, encoding="utf-8")

        return {
            "businessName": business_name,
            "slug": site_slug,
            "path": str(site_dir),
            "files": [str(path.relative_to(site_dir)) for path in files],
            "theme": config.get("theme", "sunrise-studio"),
        }

    def _index_html(self, business: dict[str, Any], analysis: dict[str, Any], config: dict[str, Any]) -> str:
        name = business.get("name", "Local Business")
        address = business.get("address", "Serving the local area")
        phone = business.get("phone", "")
        website = business.get("website", "")
        hero_headline = analysis.get("hero_headline", name)
        hero_subheadline = analysis.get("hero_subheadline", "Professional service, local trust, and a clear next step.")
        description = analysis.get("description", "A polished starter site generated from workflow results.")
        services = analysis.get("services", ["Consultation", "Core service", "Support"])
        ctas = analysis.get("calls_to_action", ["Book a call", "Request a quote"])
        sections = analysis.get("website_sections", ["Services", "Why Choose Us", "Contact"])
        include_contact = config.get("includeContactForm", True)

        services_html = "".join([f'<article class="card"><h3>{s}</h3><p>Adapt this section with pricing and local proof.</p></article>' for s in services])
        ctas_html = "".join([f'<a class="button-primary" href="#contact">{c}</a>' for c in ctas])
        tags_html = "".join([f'<span class="tag">{s}</span>' for s in sections])

        contact_html = ""
        if include_contact:
            contact_html = """
            <section id="contact" class="panel contact-form">
                <div>
                    <p class="label">Contact</p>
                    <h2>Turn this generated draft into a live site fast.</h2>
                </div>
                <form onsubmit="event.preventDefault(); alert('Form submitted!');">
                    <input placeholder="Your name" />
                    <input placeholder="Email address" />
                    <textarea placeholder="Tell us what you need" rows="5"></textarea>
                    <button type="submit" class="button-primary">Send inquiry</button>
                </form>
            </section>
            """

        return f"""<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>{name}</title>
    <link rel="stylesheet" href="style.css">
    <link rel="preconnect" href="https://fonts.googleapis.com">
    <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin>
    <link href="https://fonts.googleapis.com/css2?family=Instrument+Serif&family=Sora:wght@400;600&display=swap" rel="stylesheet">
</head>
<body>
    <main class="site-shell">
        <section class="hero">
            <p class="eyebrow">Generated by ScrapeFlow</p>
            <h1>{hero_headline}</h1>
            <p class="lede">{hero_subheadline}</p>
            <div class="hero-actions">
                {ctas_html}
            </div>
        </section>

        <section class="panel about-grid">
            <div>
                <p class="label">About</p>
                <h2>{name}</h2>
                <p>{description}</p>
            </div>
            <div class="contact-card">
                <p><strong>Address:</strong> {address}</p>
                <p><strong>Phone:</strong> {phone or 'Add phone number'}</p>
                <p><strong>Website:</strong> {website or 'Add official website'}</p>
            </div>
        </section>

        <section class="panel">
            <p class="label">Core services</p>
            <div class="grid">
                {services_html}
            </div>
        </section>

        <section class="panel">
            <p class="label">Recommended sections</p>
            <div class="tag-row">
                {tags_html}
            </div>
        </section>

        {contact_html}
    </main>
</body>
</html>"""

