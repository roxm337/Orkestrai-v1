    def _readme(self, business_name: str) -> str:
        return f"# {business_name}\n\nThis site was generated locally by ScrapeFlow.\n\n## Structure\n- `index.html`: Main page structure\n- `style.css`: Visual styling\n"

    def _slugify(self, value: str) -> str:
        return re.sub(r"[^a-z0-9]+", "-", value.lower()).strip("-") or "generated-site"
